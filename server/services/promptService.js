/**
 * AI 분석 프롬프트 생성 서비스
 */

/**
 * 프로필 데이터를 바탕으로 AI 분석 프롬프트를 생성
 */
const generateAnalysisPrompt = (profileData) => {
  const {
    interests = '',
    hobbies = '',
    personality = '',
    dreams = '',
    ideal_type = '',
    concerns = '',
    dating_style = '',
    other_info = ''
  } = profileData;

  const prompt = `
당신은 전문 심리 분석가이자 커리어 컨설턴트입니다.
다음 사용자의 프로필을 분석하여 상세한 인사이트를 제공해주세요.

[사용자 프로필]
좋아하는 것: ${interests}
취미 활동: ${hobbies}
성격 특징: ${personality}
꿈/희망: ${dreams}
이상형: ${ideal_type}
현재 고민: ${concerns}
연애 스타일: ${dating_style}
기타 정보: ${other_info}

다음 4가지 관점에서 **각각 200자 이상** 상세하게 분석해주세요:

**1. 성격 분석:**
사용자의 프로필을 바탕으로 심리학적 관점에서 성격을 상세히 분석해주세요.
- 주요 성격 특성 3-5가지 (구체적으로 설명)
- 강점 3가지와 약점 2가지 (구체적인 예시 포함)
- 행동 패턴 및 일상 생활에서의 특성
**반드시 200자 이상 작성해주세요.**

**2. 추천 진로/직업:**
프로필과 성향을 바탕으로 적합한 직업을 추천해주세요.
반드시 다음 형식을 따르세요:
"프로필을 보면 [취미/관심사/성향]에 적합한 다음 직업을 추천합니다:
1. [구체적인 직업명 예: AI 엔지니어, 데이터 분석가, 콘텐츠 크리에이터 등] - [프로필의 어떤 부분이 이 직업과 맞는지 구체적으로 설명. 예: 창의적 성향과 논리적 사고가 AI 기반 콘텐츠 제작에 적합]
2. [구체적인 직업명] - [프로필과의 연결점 구체적 설명]
3. [구체적인 직업명] - [프로필과의 연결점 구체적 설명]"
주의사항: 절대로 "필요한 역량: 창의성..." 처럼 역량만 나열하지 말고, 구체적인 직업명과 이유를 반드시 포함해야 합니다.
**반드시 200자 이상, 각 직업을 1-2줄씩 상세히 설명해주세요.**

**3. 취미 추천:**
현재 취미와 성격을 고려한 새로운 취미를 추천해주세요.
예시 형식:
"현재 취미인 [현재 취미]를 바탕으로 다음 취미를 추천합니다:
1. [구체적인 취미명] - [추천 이유], [현재 취미와의 연관성]
2. [구체적인 취미명] - [추천 이유], [성격과의 적합성]
3. [구체적인 취미명] - [추천 이유]"
**반드시 150자 이상, 각 취미를 1-2줄씩 상세히 설명해주세요.**

**4. 여행지 추천:**
성향과 취향에 맞는 여행지를 추천해주세요.
예시 형식:
"프로필을 보면 [취향/성격]에 맞는 다음 여행지를 추천합니다:
1. [구체적인 도시/지역명, 국가] - [주요 특징], [이 여행지가 적합한 이유]
2. [구체적인 도시/지역명, 국가] - [주요 특징], [이 여행지가 적합한 이유]
3. [구체적인 도시/지역명, 국가] - [주요 특징], [이 여행지가 적합한 이유]"
**반드시 150자 이상, 각 여행지를 "도시명, 국가" 형식으로 구체적으로 제시하고 한 줄씩 상세히 설명해주세요.**

중요: 각 섹션을 제목(**1. 성격 분석:**)으로 시작하고 콜론(:)으로 구분해주세요.
각 섹션은 독립적으로 작성하고, 다른 섹션의 내용을 참조하지 마세요.
`;

  return prompt;
};

module.exports = {
  generateAnalysisPrompt
};

